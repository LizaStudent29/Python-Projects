
#!/bin/bash

# Убеждаемся, что мы находимся в корне репозитория
if ! git rev-parse --is-inside-work-tree > /dev/null 2>&1; then
  echo "Ошибка: Не удается определить корень Git-репозитория."
  exit 1
fi

# Отменяем все изменения (включая неотслеживаемые файлы)
git reset --hard

# Удаляем все отслеживаемые файлы, которые не включены в .gitignore
git clean -dfx

echo "Репозиторий очищен от всех изменений и файлов."
